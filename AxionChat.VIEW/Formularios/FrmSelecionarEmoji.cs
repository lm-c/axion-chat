        private void CarregarEmojis()
        {
            flpEmojis.SuspendLayout();
            foreach (var emoji in emojis)
            {
                var btn = new Button
                {
                    Text = emoji,
                    Font = new Font("Segoe UI Emoji", 14F),
                    Size = new Size(40, 40),
                    FlatStyle = FlatStyle.Flat,
                    FlatAppearance = { BorderSize = 0 },
                    Margin = new Padding(2),
                    Cursor = Cursors.Hand
                };
